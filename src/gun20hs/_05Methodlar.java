package gun20hs;

public class _05Methodlar {

    public static void main(String[] args) {

        say();
        say();
        say();
        say();
        say();


    }


    // methodlar class icine yazilir
    // void islem yapar ama herhangi birsey return etmez
    public static void say(){
        int k = 10;

        for (int i = 0; i < k; i++) {
            System.out.print(i + " , ");
        }
    }


}
